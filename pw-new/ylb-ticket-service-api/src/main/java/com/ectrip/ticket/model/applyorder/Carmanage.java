package com.ectrip.ticket.model.applyorder;

/**
 * 车辆信息表
 * @author lijingrui
 */
public class Carmanage implements java.io.Serializable{

	/**
	 *
	 */
	private static final long serialVersionUID = -6105437141228581172L;
	//数据库字段
	private long id;//主键
	private String carno;//车牌号
	private String carnote;//车辆描述
	private String usid;//用户id
	private String note1;//String备用字段
	private long note2;//long备用字段
	private long sort;//排序
	private String note;//备注
	private String dtmaketime;//操作时间

	/**以下非数据库*/
	private String checked;//临时用  是否已选择
	private String carids;//car  id集合字符串

	public Carmanage() {
		super();
	}

	public Carmanage(long id, String carno, String carnote, String usid,
					 String note1, long note2, long sort, String note, String dtmaketime) {
		super();
		this.id = id;
		this.carno = carno;
		this.carnote = carnote;
		this.usid = usid;
		this.note1 = note1;
		this.note2 = note2;
		this.sort = sort;
		this.note = note;
		this.dtmaketime = dtmaketime;
	}

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getCarno() {
		return carno;
	}

	public void setCarno(String carno) {
		this.carno = carno;
	}

	public String getCarnote() {
		return carnote;
	}

	public void setCarnote(String carnote) {
		this.carnote = carnote;
	}

	public String getUsid() {
		return usid;
	}

	public void setUsid(String usid) {
		this.usid = usid;
	}

	public String getNote1() {
		return note1;
	}

	public void setNote1(String note1) {
		this.note1 = note1;
	}

	public long getNote2() {
		return note2;
	}

	public void setNote2(long note2) {
		this.note2 = note2;
	}

	public long getSort() {
		return sort;
	}

	public void setSort(long sort) {
		this.sort = sort;
	}

	public String getNote() {
		return note;
	}

	public void setNote(String note) {
		this.note = note;
	}

	public String getDtmaketime() {
		return dtmaketime;
	}

	public void setDtmaketime(String dtmaketime) {
		this.dtmaketime = dtmaketime;
	}

	public String getChecked() {
		return checked;
	}

	public void setChecked(String checked) {
		this.checked = checked;
	}

	public String getCarids() {
		return carids;
	}

	public void setCarids(String carids) {
		this.carids = carids;
	}


}

