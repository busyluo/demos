package com.ectrip.ticket.model.stock;

import java.util.ArrayList;
import java.util.List;

/**
 * Created by cxh on 2015/11/30.
 */
public class StockJson {
    List<StockOrderInfo> stocks;

    public StockJson() {
        super();
        stocks = new ArrayList<StockOrderInfo>();
    }

    public StockJson(List<StockOrderInfo> stocks) {
        this.stocks = stocks;
    }

    public List<StockOrderInfo> getStocks() {
        return stocks;
    }

    public void setStocks(List<StockOrderInfo> stocks) {
        this.stocks = stocks;
    }
}
