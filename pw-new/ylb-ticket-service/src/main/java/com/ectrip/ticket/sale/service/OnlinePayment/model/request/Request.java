package com.ectrip.ticket.sale.service.OnlinePayment.model.request;
public class Request {
	private String targetClass; //目标类
	private String method; //方法
	private String iticketwinid; //窗口Id
	private String iemployeeid; //操作员Id

	public Request() {
		super();
	}
	public Request(String targetClass, String method, String iticketwinid,
				   String iemployeeid) {
		super();
		this.targetClass = targetClass;
		this.method = method;
		this.iticketwinid = iticketwinid;
		this.iemployeeid = iemployeeid;
	}
	public String getTargetClass() {
		return targetClass;
	}
	public void setTargetClass(String targetClass) {
		this.targetClass = targetClass;
	}
	public String getMethod() {
		return method;
	}
	public void setMethod(String method) {
		this.method = method;
	}
	public String getIticketwinid() {
		return iticketwinid;
	}
	public void setIticketwinid(String iticketwinid) {
		this.iticketwinid = iticketwinid;
	}

	public String getIemployeeid() {
		return iemployeeid;
	}
	public void setIemployeeid(String iemployeeid) {
		this.iemployeeid = iemployeeid;
	}

}

