package com.ectrip.ticket.service.model;


public class TripControl {

	private static final long serialVersionUID = 1L;

	private String iscenicid;					  //服务商ID
	private String szscenicname;     //服务商名称
	private String itickettypeid;				  //产品ID
	private String sztickettypename; //产品名称
	private String tripid;	  //趟次ID 显示
	private String tripcode; // 场次代码
	private String tripname; // 场次名字 显示
	private String controltype;				  //数量控制模式
	private String strcontroltype;     //数量控制名称
	private String stdata;					  //日期
	private String salablenumber;				  //可售数量
	private String soldnumber;				  //已售数量
	private String starttime;				// 开始时间
	private String endtime;					// 结束时间
	private String bystate;                 //趟次状态 1可用
	private String saletype;    //销售类别 01按趟次 02按趟次区域 03按趟次区域座位
	private String strsaletype;//名称
	private String managetype; //01，区域绑定,02座位绑定
	private String strmangetype;//名称

	private String reservednumber;//预留量
	private String reservedsalenumber;//已售预留量
	private String iadvanceminute;//滯後時間

	String countString = "";//当其为空时，不显示可售数量“;1000”

	public TripControl() {
	}

	public TripControl(String iscenicid, String szscenicname,
					   String itickettypeid, String sztickettypename, String tripid,
					   String tripcode, String tripname, String controltype,
					   String strcontroltype, String stdata, String salablenumber,
					   String soldnumber, String starttime, String endtime,String bystate,
					   String saletype,String strsaletype, String managetype,String strmangetype,
					   String reservednumber,String reservedsalenumber,String iadvanceminute) {
		super();
		this.iscenicid = iscenicid;
		this.szscenicname = szscenicname;
		this.itickettypeid = itickettypeid;
		this.sztickettypename = sztickettypename;
		this.tripid = tripid;
		this.tripcode = tripcode;
		this.tripname = tripname;
		this.controltype = controltype;
		this.strcontroltype = strcontroltype;
		this.stdata = stdata;
		this.salablenumber = salablenumber;
		this.soldnumber = soldnumber;
		this.starttime = starttime;
		this.endtime = endtime;
		this.bystate = bystate;
		this.saletype = saletype;
		this.strcontroltype = strsaletype;
		this.managetype = managetype;
		this.strmangetype = strmangetype;
		this.reservednumber = reservednumber;
		this.reservedsalenumber = reservedsalenumber;
		this.iadvanceminute = iadvanceminute;
	}

	public String getIscenicid() {
		return iscenicid;
	}

	public void setIscenicid(String iscenicid) {
		this.iscenicid = iscenicid;
	}

	public String getSzscenicname() {
		return szscenicname;
	}

	public void setSzscenicname(String szscenicname) {
		this.szscenicname = szscenicname;
	}

	public String getItickettypeid() {
		return itickettypeid;
	}

	public void setItickettypeid(String itickettypeid) {
		this.itickettypeid = itickettypeid;
	}

	public String getSztickettypename() {
		return sztickettypename;
	}

	public void setSztickettypename(String sztickettypename) {
		this.sztickettypename = sztickettypename;
	}

	public String getTripid() {
		return tripid;
	}

	public void setTripid(String tripid) {
		this.tripid = tripid;
	}

	public String getTripcode() {
		return tripcode;
	}

	public void setTripcode(String tripcode) {
		this.tripcode = tripcode;
	}

	public String getTripname() {
		return tripname;
	}



	public String getIadvanceminute() {
		return iadvanceminute;
	}

	public void setIadvanceminute(String iadvanceminute) {
		this.iadvanceminute = iadvanceminute;
	}

	public void setTripname(String tripname) {
		this.tripname = tripname;
	}

	public String getControltype() {
		return controltype;
	}

	public void setControltype(String controltype) {
		this.controltype = controltype;
	}

	public String getStrcontroltype() {
		return strcontroltype;
	}

	public void setStrcontroltype(String strcontroltype) {
		this.strcontroltype = strcontroltype;
	}

	public String getStdata() {
		return stdata;
	}

	public void setStdata(String stdata) {
		this.stdata = stdata;
	}

	public String getSalablenumber() {
		return salablenumber;
	}

	public void setSalablenumber(String salablenumber) {
		this.salablenumber = salablenumber;
	}

	public String getSoldnumber() {
		return soldnumber;
	}

	public void setSoldnumber(String soldnumber) {
		this.soldnumber = soldnumber;
	}

	public String getStarttime() {
		return starttime;
	}

	public void setStarttime(String starttime) {
		this.starttime = starttime;
	}

	public String getEndtime() {
		return endtime;
	}

	public void setEndtime(String endtime) {
		this.endtime = endtime;
	}

	public String getBystate() {
		return bystate;
	}

	public void setBystate(String bystate) {
		this.bystate = bystate;
	}

	public String getSaletype() {
		return saletype;
	}

	public void setSaletype(String saletype) {
		this.saletype = saletype;
	}

	public String getStrsaletype() {
		return strsaletype;
	}

	public void setStrsaletype(String strsaletype) {
		this.strsaletype = strsaletype;
	}

	public String getManagetype() {
		return managetype;
	}

	public void setManagetype(String managetype) {
		this.managetype = managetype;
	}


	public String getStrmangetype() {
		return strmangetype;
	}

	public void setStrmangetype(String strmangetype) {
		this.strmangetype = strmangetype;
	}

	public String getCountString() {
		return countString;
	}

	public void setCountString(String countString) {
		this.countString = countString;
	}



	public String getReservednumber() {
		return reservednumber;
	}

	public void setReservednumber(String reservednumber) {
		this.reservednumber = reservednumber;
	}

	public String getReservedsalenumber() {
		return reservedsalenumber;
	}

	public void setReservedsalenumber(String reservedsalenumber) {
		this.reservedsalenumber = reservedsalenumber;
	}

	@Override
	public String toString() {
		return tripname+"("+starttime+"-"+endtime+(countString == null?"":","+(Integer.parseInt(salablenumber)-Integer.parseInt(soldnumber))) +")";
	}


}


