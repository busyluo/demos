public class QueryBillRequest extends Request {

    public String oto;

    public String orid;

    public String getOto() {
        return oto;
    }

    public void setOto(String oto) {
        this.oto = oto;
    }

    public String getOrid() {
        return orid;
    }

    public void setOrid(String orid) {
        this.orid = orid;
    }

    @Override
    public String toString() {
        return "QueryBillRequest{" +
                "oto='" + oto + '\'' +
                ", orid='" + orid + '\'' +
                '}';
    }
}
