public class TicketStation  implements Serializable {

	private static final long serialVersionUID = 1L;

	private String iticketstationid;           // 门票站点ID
	private String iscenicid;						//景区景点ID
	private String szstationcode;				//门票站点代码
	private String szstationname;				//门票站点名称
	private String szcontact;					//联系人
	private String szphone;						//联系电话
	private String szaddress;					//实际地址
	private String byisuse;						//启用状态
	private String szmemo;						//备注
	private String iversion;



	public TicketStation() {
	}
	public TicketStation(String iticketstationid, String iscenicid,
						 String szstationcode, String szstationname, String szcontact,
						 String szphone, String szaddress, String byisuse, String szmemo,
						 String iversion) {
		super();
		this.iticketstationid = iticketstationid;
		this.iscenicid = iscenicid;
		this.szstationcode = szstationcode;
		this.szstationname = szstationname;
		this.szcontact = szcontact;
		this.szphone = szphone;
		this.szaddress = szaddress;
		this.byisuse = byisuse;
		this.szmemo = szmemo;
		this.iversion = iversion;
	}
	public String getIticketstationid() {
		return iticketstationid;
	}
	public void setIticketstationid(String iticketstationid) {
		this.iticketstationid = iticketstationid;
	}
	public String getIscenicid() {
		return iscenicid;
	}
	public void setIscenicid(String iscenicid) {
		this.iscenicid = iscenicid;
	}
	public String getSzstationcode() {
		return szstationcode;
	}
	public void setSzstationcode(String szstationcode) {
		this.szstationcode = szstationcode;
	}
	public String getSzstationname() {
		return szstationname;
	}
	public void setSzstationname(String szstationname) {
		this.szstationname = szstationname;
	}
	public String getSzcontact() {
		return szcontact;
	}
	public void setSzcontact(String szcontact) {
		this.szcontact = szcontact;
	}
	public String getSzphone() {
		return szphone;
	}
	public void setSzphone(String szphone) {
		this.szphone = szphone;
	}
	public String getSzaddress() {
		return szaddress;
	}
	public void setSzaddress(String szaddress) {
		this.szaddress = szaddress;
	}
	public String getByisuse() {
		return byisuse;
	}
	public void setByisuse(String byisuse) {
		this.byisuse = byisuse;
	}
	public String getSzmemo() {
		return szmemo;
	}
	public void setSzmemo(String szmemo) {
		this.szmemo = szmemo;
	}
	public String getIversion() {
		return iversion;
	}
	public void setIversion(String iversion) {
		this.iversion = iversion;
	}

}
