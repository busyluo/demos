package com.ectrip.ticket.sale.service.OnlinePayment.model.response;

/**
 * Created by cxh on 2016/07/20.
 */
public class AddAndPayBillResponse extends Response {

    public String oto;//oto标识

    public String orid;//订单号

    public String amount;//金额

    public String status;//订单状态

    public String qrCodeUrl;//二维码链接

    public String getOto() {
        return oto;
    }

    public void setOto(String oto) {
        this.oto = oto;
    }

    public String getOrid() {
        return orid;
    }

    public void setOrid(String orid) {
        this.orid = orid;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getQrCodeUrl() {
        return qrCodeUrl;
    }

    public void setQrCodeUrl(String qrCodeUrl) {
        this.qrCodeUrl = qrCodeUrl;
    }

    @Override
    public String toString() {
        return "AddAndPayBillResponse{" +
                "oto='" + oto + '\'' +
                ", orid='" + orid + '\'' +
                ", amount='" + amount + '\'' +
                ", status='" + status + '\'' +
                ", qrCodeUrl='" + qrCodeUrl + '\'' +
                '}';
    }
}
