package com.ectrip.ec.model.order;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name="T_REALNAME")
public class TRealname implements java.io.Serializable {

     
    @Id
 	private Long seq;
 	@Column(name="ORID")
 	private String orid;
 	@Column(name="ITICKETTYPEID")
 	private Long itickettypeid;
 	@Column(name="ISCENICID")
 	private Long iscenicid;
 	@Column(name="ICROWDKINDID")
 	private Long icrowdkindid;
 	@Column(name="CNAME")
 	private String cname;
 	@Column(name="IDCARD")
 	private String idcard;
 	@Column(name="ZJTP")
 	private String zjtp;
 	@Column(name="ISCHILD")
 	private Long ischild;
 	@Column(name="MBNUMBER")
 	private String mbnumber;


    // Constructors

    /** default constructor */
    public TRealname() {
    }

	/** minimal constructor */
    public TRealname(Long seq, String orid, Long itickettypeid, Long iscenicid, Long icrowdkindid, String cname, Long ischild) {
        this.seq = seq;
        this.orid = orid;
        this.itickettypeid = itickettypeid;
        this.iscenicid = iscenicid;
        this.icrowdkindid = icrowdkindid;
        this.cname = cname;
        this.ischild = ischild;
    }
    
    /** full constructor */
    public TRealname(Long seq, String orid, Long itickettypeid, Long iscenicid, Long icrowdkindid, String cname, String idcard, String zjtp, Long ischild, String mbnumber) {
        this.seq = seq;
        this.orid = orid;
        this.itickettypeid = itickettypeid;
        this.iscenicid = iscenicid;
        this.icrowdkindid = icrowdkindid;
        this.cname = cname;
        this.idcard = idcard;
        this.zjtp = zjtp;
        this.ischild = ischild;
        this.mbnumber = mbnumber;
    }

   
    // Property accessors

    public Long getSeq() {
        return this.seq;
    }
    
    public void setSeq(Long seq) {
        this.seq = seq;
    }

    public String getOrid() {
        return this.orid;
    }
    
    public void setOrid(String orid) {
        this.orid = orid;
    }

    public Long getItickettypeid() {
        return this.itickettypeid;
    }
    
    public void setItickettypeid(Long itickettypeid) {
        this.itickettypeid = itickettypeid;
    }

    public Long getIscenicid() {
        return this.iscenicid;
    }
    
    public void setIscenicid(Long iscenicid) {
        this.iscenicid = iscenicid;
    }

    public Long getIcrowdkindid() {
        return this.icrowdkindid;
    }
    
    public void setIcrowdkindid(Long icrowdkindid) {
        this.icrowdkindid = icrowdkindid;
    }

    public String getCname() {
        return this.cname;
    }
    
    public void setCname(String cname) {
        this.cname = cname;
    }

    public String getIdcard() {
        return this.idcard;
    }
    
    public void setIdcard(String idcard) {
        this.idcard = idcard;
    }

    public String getZjtp() {
        return this.zjtp;
    }
    
    public void setZjtp(String zjtp) {
        this.zjtp = zjtp;
    }

    public Long getIschild() {
        return this.ischild;
    }
    
    public void setIschild(Long ischild) {
        this.ischild = ischild;
    }

    public String getMbnumber() {
        return this.mbnumber;
    }
    
    public void setMbnumber(String mbnumber) {
        this.mbnumber = mbnumber;
    }
   








}
