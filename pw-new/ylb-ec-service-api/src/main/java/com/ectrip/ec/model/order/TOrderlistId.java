package com.ectrip.ec.model.order;

import javax.persistence.Embeddable;

@Embeddable
public class TOrderlistId implements java.io.Serializable {

	// Fields

	private Long orderlistid;//明细流水
	private String orid;//订单号
	private Long iscenicid;//服务商

	// Constructors

	/** default constructor */
	public TOrderlistId() {
	}

	/** full constructor */
	public TOrderlistId(Long orderlistid,String orid,Long iscenicid) {
		this.orderlistid = orderlistid;
		this.orid = orid;
		this.iscenicid=iscenicid;
	}

	// Property accessors

	public Long getOrderlistid() {
		return this.orderlistid;
	}

	public void setOrderlistid(Long orderlistid) {
		this.orderlistid = orderlistid;
	}

	public String getOrid() {
		return orid;
	}

	public void setOrid(String orid) {
		this.orid = orid;
	}

	public Long getIscenicid() {
		return iscenicid;
	}

	public void setIscenicid(Long iscenicid) {
		this.iscenicid = iscenicid;
	}





}
