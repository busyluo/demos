package com.ectrip.hqyt.model.response;

import java.math.BigDecimal;

import com.ectrip.hqyt.model.enums.RefundBillStatus;

public class JSONRefundBill {
	
	private JSONInvoice invoice;
	private BigDecimal amount;
	private String reason;
	private String outRefundNo;
	private RefundBillStatus status;
	public JSONInvoice getInvoice() {
		return invoice;
	}

	public void setInvoice(JSONInvoice invoice) {
		this.invoice = invoice;
	}

	public BigDecimal getAmount() {
		return amount;
	}

	public void setAmount(BigDecimal amount) {
		this.amount = amount;
	}

	public String getReason() {
		return reason;
	}

	public void setReason(String reason) {
		this.reason = reason;
	}

	public String getOutRefundNo() {
		return outRefundNo;
	}

	public void setOutRefundNo(String outRefundNo) {
		this.outRefundNo = outRefundNo;
	}

	public RefundBillStatus getStatus() {
		return status;
	}

	public void setStatus(RefundBillStatus status) {
		this.status = status;
	}
}
