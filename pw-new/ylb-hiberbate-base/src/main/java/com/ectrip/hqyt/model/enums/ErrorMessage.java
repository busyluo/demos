package com.ectrip.hqyt.model.enums;

/**
 * 错误消息
 */
public enum ErrorMessage {

    UNKNOWN_ERROR("系统发生未知错误"),
    PARAMS_ERROR("参数不正确"),
    ORDER_CREATE_FAIL("订单创建失败"),
    DATA_REQ_FAIL("数据请求失败");

    ErrorMessage(String msg){
        this.text = msg;
    }

    private String text;

    public String getText(){
        return this.text;
    }
}
