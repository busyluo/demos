package com.ectrip.hqyt.model.enums;

/**
 * 交易单退款状态
 * @author zuotongcheng
 */
public enum InvoiceRefundStatus {

	NO_REFUND("无退款"),
	REFUNDING("退款中"),
	REFUNDED("退款结束");
	
	InvoiceRefundStatus(String text) {
		this.text = text;
	}
	
	private String text;

	public String getText() {
		return text;
	}
	
}
